    public void showPersonalRecord(MemberPersonalInformationBean objPersonal) {
        txtMemberId.setText(String.valueOf(objPersonal.getMemberID()));
        txtDOB.setText(objPersonal.getDOB());
        txtPhone.setText(objPersonal.getPhone());
        TxtEmail.setText(objPersonal.getEmail());
    }

    public void showSportRecord(SportBean objSport) {
        txtSportId.setText(String.valueOf(objSport.getSportId()));
        txtFees.setText(String.valueOf(objSport.getFees()));
        txtDuration.setText(String.valueOf(objSport.getDuration()));

    }

