    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        if (txtName.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "UserName is required");
            txtName.grabFocus();

        } else {
            String result = "";
            objBean = new MemberPersonalInformationBean();
            objBean.setMemberID(Integer.parseInt(txtID.getText()));
            objBean.setName(txtName.getText());
            objBean.setAddress(txtAddress.getText());
            objBean.setDOB(txtDOB.getText());
            objBean.setEmail(txtEmailID.getText());
            objBean.setGender(txtGender.getText());
            objBean.setMobile(txtMobile.getText());
            objBean.setOccupation(txtOccupation.getText());
            objBean.setPhone(txtPhone.getText());
            objBean.setStatus(ddlStatus.getSelectedItem().toString());
            if (addUpdateFlag.equalsIgnoreCase("add")) {
                result = objDB.addMemberDetails(objBean);
            } else if (addUpdateFlag.equalsIgnoreCase("Update")) {
                result = objDB.updateMemberDetails(objBean);
            }
            if (result.equalsIgnoreCase("added")) {
                JOptionPane.showMessageDialog(this, "Record added successfuly");
                lst = objDB.getAllMemberPersonalInformation();
                count = lst.size() - 1;
                showRecord(lst.get(count));
                disable(false);
                disableButton(true);

            } else if (result.equalsIgnoreCase("updated")) {
                JOptionPane.showMessageDialog(this, "Record updated successfuly");
                lst = objDB.getAllMemberPersonalInformation();
//                count = lst.size() - 1;
                showRecord(lst.get(count));
                disable(false);
                disableButton(true);
            } else if (result.equalsIgnoreCase("exists")) {
                JOptionPane.showMessageDialog(this, "Record already exists");

            } else {
                JOptionPane.showMessageDialog(this, "Operation failed");
            }

        }
    }//GEN-LAST:event_btnSaveActionPerformed

