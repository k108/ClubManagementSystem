    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (txtTypeName.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "UserName is required");
            txtTypeName.grabFocus();

        } else if (txtDesc.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Name is required");
            txtDesc.grabFocus();
        } else if (txtFactor.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Contact is required");
            txtFactor.grabFocus();
        } else {
            String result = "";
            objBean = new ManageMembershipBean();

            objBean.setMembershipId(Integer.parseInt(txtTypeId.getText()));

            objBean.setTypeName(txtTypeName.getText());
            objBean.setDescription(txtDesc.getText());
            objBean.setFactor(Integer.parseInt(txtFactor.getText()));

            objBean.setStatus(ddlStatus.getSelectedItem().toString());

            if (addUpdateFlag.equalsIgnoreCase("add")) {
                result = objDB.addMemberships(objBean);
            } else if (addUpdateFlag.equalsIgnoreCase("Update")) {
                result = objDB.updateMembership(objBean);
            }
            if (result.equalsIgnoreCase("added")) {
                JOptionPane.showMessageDialog(this, "Record added successfuly");
                lst = objDB.getAllMemberships();
                count = lst.size() - 1;
                showRecord(lst.get(count));
                disable(false);
                disableButton(true);

            } else if (result.equalsIgnoreCase("updated")) {
                JOptionPane.showMessageDialog(this, "Record updated successfuly");
                lst = objDB.getAllMemberships();
                count = lst.size() - 1;
                showRecord(lst.get(count));
                disable(false);
                disableButton(true);
            } else if (result.equalsIgnoreCase("exists")) {
                JOptionPane.showMessageDialog(this, "Record already exists");

            } else {
                JOptionPane.showMessageDialog(this, "Operation failed");
            }

        }
    }//GEN-LAST:event_btnSaveActionPerformed

