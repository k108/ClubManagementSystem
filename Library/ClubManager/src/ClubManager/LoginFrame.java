    private void btnForgotPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnForgotPasswordActionPerformed
        // TODO add your handling code here:
        objDB = new DBOperations();
        String un = txtUsername.getText();
        String result = objDB.forgotPassword(un);

        if (result.equalsIgnoreCase("sent")) {
            JOptionPane.showMessageDialog(this, "Your password has been sent to your email");
        } else {
            JOptionPane.showMessageDialog(this, "Failure sending email");
        }
    }//GEN-LAST:event_btnForgotPasswordActionPerformed

